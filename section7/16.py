import numpy as np

a = np.array([1, 2, 3])
b = np.array([4, 5, 6])

# a + b
result_add_ab = np.add(a, b)
print(result_add_ab)

# a - b
result_sub_ab = np.subtract(a, b)
print(result_sub_ab)

# a * b
result_mul_ab = np.multiply(a, b)
print(result_mul_ab)

# a / b
result_div_ab = np.divide(a, b)
print(result_div_ab)